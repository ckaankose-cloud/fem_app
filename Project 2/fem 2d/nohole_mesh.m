function [NL, EL, NoE, NoN] = nohole_mesh(p_input)

d1 = 1;
d2 = 1;
q = [0 0; d1 0; 0 d2; d1 d2];
p =p_input;
m = p_input;

PD = 2;

NoN = (p+1)*(m+1);




%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%   Nodes   %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

NL = zeros(NoN, PD);

a = d1/p;
b = d2/m;

n=1;

for i = 1:m+1
    
   for j = 1:p+1
       
       NL(n,1) = q(1,1) + (j-1)*a;
       NL(n,2) = q(1,2) + (i-1)*b;
       n = n+1;
       
   end
   
end


       

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%   Elements   %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
EL=[];
a=[];
b=[];
for i = 1:p+1
    for k = (i-1)*p+i : i*p+(i-1)
        a =[k, k+1];
        b =[b;a];
    end
end
EL=[EL;b];
c=[];
d=[];
for i=1: (p+1)*(p+1)-(p+1)
    c=[i, i+p+1];
    d=[d;c];
end
EL=[EL;d];
e=[];
f=[];
for i = 1:p
    for k = (i-1)*p+i : i*p+i-1
        e = [k, k+p+2];
        f=[f;e];
    end
end
EL=[EL;f];

NoN = size(NL,1);
NoE = size(EL,1);